package myJava;

public class Операторы {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("Hello\nWorld!"); // \n - перевод на новую строку
        System.out.println("Hello\n\n\n\nWorld!"); // \n\n\n\n- пустые строки
        System.out.println("Hello\t\t\tWorld!"); // \t\t\t- пробелы слева
        System.out.println("Hello\" World!"); // \ - для восприятия ковычки "
        System.out.println("Hello\\ World!");// \ - для восприятия слеша \
    }
}
// public static void main(String[] args) => сокращенно "psvm"
// System.out.println(); => сокращенно "sout"
// println - с переводом на новую строчку
// print - без перевода на новую строчку
