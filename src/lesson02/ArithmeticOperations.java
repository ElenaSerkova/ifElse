package lesson02;

public class ArithmeticOperations {
    public static void main(String[] args) {
        System.out.println("4 + 4 = " + 8);

        int x = 32;
        int y = 16;

        System.out.println(x / y);

        x = 35;
        y = 5;

        System.out.println(x * y);

        x = 56;
        y = 34;

        System.out.println(x - y);

        x = 12;
        y = 67;

        int result = x + y;

        System.out.println(result);

        double xDouble = 12.7;
        double yDouble = 14.6;

        double doubleResult = xDouble + yDouble;

        System.out.println(doubleResult);

        xDouble = 3.5;
        yDouble = 2.5;

        System.out.println(xDouble - yDouble);

    }
}
