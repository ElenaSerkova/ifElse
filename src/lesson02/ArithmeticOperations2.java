package lesson02;

public class ArithmeticOperations2 {
    public static void main(String[] args) {
        int x = 25;
        int y = 5;
        System.out.println(x / y);

        x = 12;
        y = 7;

        System.out.println(x / y);
        System.out.println(x % y);

        double xDouble = x;

        System.out.println(xDouble / y);

        double doubleResults = (double) x / y;

        System.out.println(doubleResults);

        double yDouble = 9.2;

        System.out.println(xDouble + yDouble);

        System.out.println(xDouble * yDouble);

        double xDouble2 = 15;
        double yDouble2 = 4;

        System.out.println(xDouble2 / yDouble2);
        System.out.println(xDouble2 % yDouble2);


    }
}
